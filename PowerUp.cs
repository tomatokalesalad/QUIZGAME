/// <summary>
/// Represents available power-ups in the quiz game.
/// </summary>
public enum PowerUp
{
    /// <summary>
    /// Removes two incorrect options from a question.
    /// </summary>
    FiftyFifty,
    /// <summary>
    /// Skips the current question.
    /// </summary>
    SkipQuestion,
    /// <summary>
    /// Doubles the points for a correct answer.
    /// </summary>
    DoublePoints
}
